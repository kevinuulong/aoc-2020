function countTrees(slope){
    var pos = [0,0];
    var trees = 0;
    while (pos[1]<lines.length) {
        if (lines[pos[1]%lines.length].substring(pos[0]%31,(pos[0]%31)+1)=='#') {
            trees++;
        }
        pos[0] += slope[0];
        pos[1] += slope[1];
    }
    return trees;
}
